#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
int main() {
	int t; scanf("%d", &t);
	while (t--) {
		int n; scanf("%d", &n);
		vector<int> s1(n);
		for (int i = 0; i < n; ++i)
			scanf("%d", &s1[i]);

		int q; scanf("%d", &q);
		vector<ll> s2;
		for (int i = 0; i < q; ++i)
			scanf("%lld", &s2[i]);
	}
}